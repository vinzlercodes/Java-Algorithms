package ExpressionsAndCalculationsWithUserInput;

public class Expressions {

    public static void main(String[] args) {

        /*
         the following method prints out the addition, subtraction, multiplication and division
         of sample input numbers
         */

        int a = 25;

        float b = 7.6f;

        float z = a + b;

        float g = b - a;

        float y = a * b;

        float x = b / a;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        System.out.println(g);

    }// end of main
}// end of class