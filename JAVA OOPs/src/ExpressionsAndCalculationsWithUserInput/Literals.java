package ExpressionsAndCalculationsWithUserInput;

public class Literals {
    public static void main(String[] args) {
        /*
         the following method prints out the various literal values of:
         integer, floating point number, character, string and boolean
         with sample inputs
         */


        int integer = 420;

        float decimal = 6.9f;

        char character = 'V';

        String sentence = "Hello! My name is Vinayak";

        boolean tf = false;

        System.out.println(integer);
        System.out.println(decimal);
        System.out.println(character);
        System.out.println(sentence);
        System.out.println(tf);
    }// end of main
}// end of class